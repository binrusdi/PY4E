import math
# Mengembalikan akar kudadrat suatu angka
result_kuadrat = math.sqrt(64)
print(result_kuadrat) # 8.0

# Membulatkan angka ke atas
result_bulat_keatas = math.ceil(2.4)
print(result_bulat_keatas) # 3

# Membulatkan angka ke bawah
result_bulat_kebawah = math.floor(2.4)
print(result_bulat_kebawah) # 2

# Konstanta PI, mengembalikan 3.14
